// ---------------------------------------------------------------------------

export function exportLoanDisbursementsPdf(loan: Loan) {
  const doc = new jsPDF({ unit: "mm", format: "a4" })
  const colW = doc.internal.pageSize.getWidth() - MARGIN * 2

  let y = drawHeader(doc, "Artha — Loan Disbursements", loan.name)

  const cols: Column[] = [
    { label: "Date", x: MARGIN, w: 30, align: "left" },
    { label: "Notes", x: MARGIN + 30, w: colW - 30 - 40, align: "left" },
    { label: "Amount", x: MARGIN + colW - 40, w: 40, align: "right" },
  ]

  y = drawColumnHeader(doc, cols, y, colW)

  const disbursements = [...loan.disbursements].sort(
    (a, b) => new Date(a.date).getTime() - new Date(b.date).getTime()
  )

  doc.setFont("helvetica", "normal")
  doc.setFontSize(8)
  let total = 0

  disbursements.forEach((d, idx) => {
    if (y > 270) {
      doc.addPage()
      y = drawColumnHeader(doc, cols, 14, colW)
      doc.setFont("helvetica", "normal")
      doc.setFontSize(8)
    }
    const shade = idx % 2 === 0 ? 255 : 249
    doc.setFillColor(shade, shade, shade)
    doc.rect(MARGIN, y, colW, ROW_H, "F")
    doc.setTextColor(40, 40, 40)

    doc.text(shortDate(d.date), cols[0].x + 1, y + 5)
    doc.text((d.notes ?? "—").slice(0, 70), cols[1].x + 1, y + 5)
    doc.text(pdfINR(Number(d.amount)), cols[2].x + cols[2].w - 1, y + 5, { align: "right" })

    total += Number(d.amount)
    y += ROW_H
  })

  drawTotalRow(doc, y, colW, "Total", pdfINR(total))
  drawFooter(doc)
  doc.save(safeFileName(loan.name, "Disbursements"))
}

/** Emerald-tinted total row spanning the table width (label left, amount right). */
function drawTotalRow(doc: jsPDF, y: number, colW: number, label: string, amount: string) {
  if (y > 270) {
    doc.addPage()
    y = 14
  }
  doc.setFillColor(220, 240, 225)
  doc.rect(MARGIN, y, colW, 8, "F")
  doc.setFont("helvetica", "bold")
  doc.setFontSize(9)
  doc.setTextColor(20, 80, 40)
  doc.text(label, MARGIN + 1, y + 5.5)
  doc.text(amount, MARGIN + colW - 1, y + 5.5, { align: "right" })
}
